import {
  readdirSync,
  readFileSync,
  watch as watchFiles,
  writeFileSync
} from "node:fs"
import path from "node:path"

const REPOSITORY_ROOT = path.resolve(__dirname, "..")
const DEFINITIONS_ROOT = path.join(
  REPOSITORY_ROOT,
  "app/config/game/ability-definitions"
)
const REGISTRY_PATH = path.join(DEFINITIONS_ROOT, "registry.generated.ts")
const CHECK_ONLY = process.argv.includes("--check")
const WATCH = process.argv.includes("--watch")
const NEWLINE = process.platform === "win32" ? "\r\n" : "\n"
const INFRASTRUCTURE_FILES = new Set([
  "define-ability.ts",
  "index.ts",
  "registry.generated.ts"
])

function isDefinitionFile(fileName: string): boolean {
  return fileName.endsWith(".ts") && !INFRASTRUCTURE_FILES.has(fileName)
}

function toIdentifier(fileName: string): string {
  const baseName = fileName.replace(/\.ts$/, "")
  const camelCase = baseName.replace(/-([a-z0-9])/g, (_, character: string) =>
    character.toUpperCase()
  )
  return `${camelCase}Definition`
}

function getDefinitionFiles(): string[] {
  return readdirSync(DEFINITIONS_ROOT).filter(isDefinitionFile).sort()
}

function createRegistry(definitionFiles: string[]): string {
  if (definitionFiles.length === 0) {
    throw new Error("No ability definition files found")
  }

  const imports = definitionFiles
    .map((fileName) => {
      const moduleName = fileName.replace(/\.ts$/, "")
      return `import ${toIdentifier(fileName)} from "./${moduleName}"`
    })
    .join("\n")
  const entries = definitionFiles
    .map((fileName) => {
      const identifier = toIdentifier(fileName)
      return `  [${identifier}.ability]: ${identifier}`
    })
    .join(",\n")

  return `// This file is generated by gen/generate-ability-definition-registry.ts.
// The development watcher and production build keep it up to date automatically.

${imports}

export const AbilityDefinitions = Object.freeze({
${entries}
})
`.replaceAll("\n", NEWLINE)
}

function normalized(content: string): string {
  return content.replaceAll("\r\n", "\n")
}

function registryIsCurrent(generated: string): boolean {
  try {
    return (
      normalized(readFileSync(REGISTRY_PATH, "utf8")) === normalized(generated)
    )
  } catch (error) {
    if ((error as NodeJS.ErrnoException).code === "ENOENT") return false
    throw error
  }
}

function updateRegistry(): boolean {
  const definitionFiles = getDefinitionFiles()
  const generated = createRegistry(definitionFiles)
  if (registryIsCurrent(generated)) return false

  writeFileSync(REGISTRY_PATH, generated, "utf8")
  console.log(
    `Generated ability definition registry with ${definitionFiles.length} definitions.`
  )
  return true
}

if (CHECK_ONLY) {
  const definitionFiles = getDefinitionFiles()
  if (!registryIsCurrent(createRegistry(definitionFiles))) {
    console.error(
      "Ability definition registry is stale. Run `npm run generate-ability-definition-registry`."
    )
    process.exitCode = 1
  } else {
    console.log(
      `Ability definition registry is current (${definitionFiles.length} definitions).`
    )
  }
} else {
  const changed = updateRegistry()
  if (!changed && !WATCH) {
    console.log(
      `Ability definition registry is already current (${getDefinitionFiles().length} definitions).`
    )
  }
}

if (WATCH) {
  console.log("Watching ability definition files for registry changes.")
  let pendingUpdate: ReturnType<typeof setTimeout> | undefined
  watchFiles(DEFINITIONS_ROOT, (_eventType, fileName) => {
    if (fileName && !isDefinitionFile(fileName)) return

    if (pendingUpdate) clearTimeout(pendingUpdate)
    pendingUpdate = setTimeout(() => {
      pendingUpdate = undefined
      try {
        updateRegistry()
      } catch (error) {
        console.error("Failed to update ability definition registry:", error)
      }
    }, 100)
  })
}
